// string => collection of characters

const str = "Hello World"

console.log(str.length) // length of the string

console.log(str[9]); // character at index 9
console.log(str.charAt(4)); // character at index 4

console.log(str.toUpperCase()) // convert to uppercase
console.log(str.toLowerCase()) // convert to lowercase

console.log(str.indexOf("lleo")) // index of the character

console.log(str.includes("World")) // check if the string contains the character;

console.log(str.slice(1, 2)) // slice string from index 2 to index 4

console.log(str.split("l")) // split string into array

console.log(str.replaceAll("l", "5")) // replace string;

